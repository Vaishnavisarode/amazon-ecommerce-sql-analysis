--  6.Identify the least-selling product category for each state and include the  total sales for that category within each state. 

WITH category_sales AS (
SELECT  c.state,
		cat.category_name,
        ROUND(SUM(oi.quantity * oi.price_per_unit), 2) AS total_sales
FROM customers c
JOIN orders o
ON c.customer_id = o.customer_id
JOIN order_items oi
ON o.order_id = oi.order_id
JOIN products p
ON oi.product_id = p.product_id
JOIN category cat
ON p.category_id = cat.category_id
GROUP BY c.state, cat.category_name),
ranked_sales AS (
SELECT *,
		RANK() OVER (
			PARTITION BY state
			ORDER BY total_sales ASC
		) AS rnk
FROM category_sales)
SELECT
    state,
    category_name as least_selling_category,
    total_sales
FROM ranked_sales
WHERE rnk = 1
ORDER BY state
;

-- 7.Calculate the customer lifetime value (CLTV) for each customer and rank customers based on their CLTV.
SELECT c.customer_id,
	   concat(c.first_name ,' ', c.last_name) as customer_name,
	   round(sum(oi.price_per_unit * oi.quantity),2) as customer_lifetime_value,
       dense_rank() over( order by sum(oi.price_per_unit * oi.quantity)desc) as rank_customers
from order_items oi
join orders o
on oi.order_id=o.order_id
join customers c
on o.customer_id=c.customer_id
group by c.customer_id, customer_name
order by rank_customers 
;


