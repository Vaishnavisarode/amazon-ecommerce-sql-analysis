-- 2.Calculate total revenue generated by each product category and include the percentage contribution of each category to total revenue.
SELECT
    c.category_name,
    ROUND(SUM(oi.quantity * oi.price_per_unit), 2) AS total_revenue,
    ROUND(
        SUM(oi.quantity * oi.price_per_unit) * 100.0 /
        SUM(SUM(oi.quantity * oi.price_per_unit)) OVER (),
        2
    ) AS revenue_percentage
FROM order_items oi
JOIN products p
    ON oi.product_id = p.product_id
JOIN category c
    ON p.category_id = c.category_id
GROUP BY c.category_name
ORDER BY total_revenue DESC;

-- 3.Compute the average order value (AOV) for each customer and include only customers with more than 5 orders.
WITH order_total AS (
    SELECT
        o.order_id,
        o.customer_id,
        SUM(oi.quantity * oi.price_per_unit) AS order_value
    FROM orders o
    JOIN order_items oi
        ON o.order_id = oi.order_id
    GROUP BY o.order_id, o.customer_id
)
SELECT
    customer_id,
    COUNT(order_id) AS total_orders,
    ROUND(AVG(order_value), 2) AS average_order_value
FROM order_total
GROUP BY customer_id
HAVING COUNT(order_id) > 5
ORDER BY average_order_value DESC;

-- 4.Query monthly total sales over the past year and display the current month’s sales along with the previous month’s sales.
WITH monthly_sales AS (
    SELECT
        DATE_FORMAT(o.order_date, '%Y-%m') AS sales_month,
        ROUND(SUM(oi.quantity * oi.price_per_unit), 2) AS total_sales
    FROM orders o
    JOIN order_items oi
        ON o.order_id = oi.order_id
    GROUP BY DATE_FORMAT(o.order_date, '%Y-%m')
)

SELECT
    sales_month,
    total_sales AS current_month_sales,
    LAG(total_sales) OVER (ORDER BY sales_month) AS previous_month_sales
FROM monthly_sales
ORDER BY sales_month;

